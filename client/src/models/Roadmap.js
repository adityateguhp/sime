class Roadmap {
    constructor(_id, roadmap_name, roadmap_start_date, roadmap_end_date, event_id) {
        this._id = _id;
        this.roadmap_name = roadmap_name;
        this.roadmap_start_date = roadmap_start_date;
        this.roadmap_end_date = roadmap_end_date;
        this.event_id = event_id;
    }
}

export default Roadmap;